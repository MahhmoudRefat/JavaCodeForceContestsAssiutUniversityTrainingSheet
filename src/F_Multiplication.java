import java.util.Scanner;

public class F_Multiplication {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = 1 ; i <= 12 ; i++) {
            System.out.println(n + " * " + i + " = " + (n*i) );
        }
    }



}
